package com.pitapany.web.entity;

public class MemberProfile {
	private String id;
	private String job;
	private String bloodType;
	private String imgSrc;
	private String imgName;
	private String backImgSrc;
	private String backImgName;
	private String status;
	private String kakaotalkId;
	private String facebookUrl;
	private String instagramUrl;
	private String memberId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getJob() {
		return job;
	}

	public void setJob(String job) {
		this.job = job;
	}

	public String getBloodType() {
		return bloodType;
	}

	public void setBloodType(String bloodType) {
		this.bloodType = bloodType;
	}

	public String getImgSrc() {
		return imgSrc;
	}

	public void setImgSrc(String imgSrc) {
		this.imgSrc = imgSrc;
	}

	public String getImgName() {
		return imgName;
	}

	public void setImgName(String imgName) {
		this.imgName = imgName;
	}

	public String getBackImgSrc() {
		return backImgSrc;
	}

	public void setBackImgSrc(String backImgSrc) {
		this.backImgSrc = backImgSrc;
	}

	public String getBackImgName() {
		return backImgName;
	}

	public void setBackImgName(String backImgName) {
		this.backImgName = backImgName;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getKakaotalkId() {
		return kakaotalkId;
	}

	public void setKakaotalkId(String kakaotalkId) {
		this.kakaotalkId = kakaotalkId;
	}

	public String getFacebookUrl() {
		return facebookUrl;
	}

	public void setFacebookUrl(String facebookUrl) {
		this.facebookUrl = facebookUrl;
	}

	public String getInstagramUrl() {
		return instagramUrl;
	}

	public void setInstagramUrl(String instagramUrl) {
		this.instagramUrl = instagramUrl;
	}

	public String getMemberId() {
		return memberId;
	}

	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}

}
